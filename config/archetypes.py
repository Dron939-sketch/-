# Quick map by code
_BY_CODE: Dict[str, Archetype] = {a["code"]: a for a in ARCHETYPES}


def get(code: str) -> Optional[Archetype]:
    return _BY_CODE.get(code)


def all_codes() -> List[str]:
    return [a["code"] for a in ARCHETYPES]


# ---------------------------------------------------------------------------
# Подбор архетипа по депутату
# ---------------------------------------------------------------------------

def suggest_for_deputy(deputy: Dict[str, Any]) -> Archetype:
    """Подбор архетипа по данным депутата:
      - role=speaker (председатель/замы) → Правитель / Мудрец
      - 5+ секторов из leadership → Правитель
      - сектор соцзащита/здравоохранение → Заботливый
      - молодёжь/образование/культура → Создатель / Близкий
      - benchmark: первый sectors_fit-матч.

    Если ничего не подошло — Свой (everyman) как нейтральный default.
    """
    role = (deputy.get("role") or "").lower()
    sectors = [s.lower() for s in (deputy.get("sectors") or [])]

    # Председатель Совета — Правитель
    note = (deputy.get("note") or "").lower()
    if "председатель" in note and "заместитель" not in note:
        return _BY_CODE["ruler"]

    # Speaker / sector_lead — Мудрец (объясняет сложное на цифрах)
    if role == "speaker" and len(sectors) >= 5:
        return _BY_CODE["sage"]

    # По первому сектору находим архетип, в чьём sectors_fit он есть.
    for sec in sectors:
        for a in ARCHETYPES:
            if sec in [f.lower() for f in (a.get("sectors_fit") or [])]:
                return a

    # Default — Свой
    return _BY_CODE["everyman"]


def archetype_match_score(post_text: str, archetype: Archetype) -> float:
    """Грубая heuristic: 0..1 насколько post соответствует voice архетипа.

    Используется для аудита — насколько фактический стиль соответствует
    рекомендованному. Без LLM, простой keyword/паттерн scoring.
    """
    if not post_text:
        return 0.0
    txt = post_text.lower()
    score = 0.0
    code = archetype["code"]

    # Простые сигналы по архетипам
    if code == "caregiver":
        if any(w in txt for w in ("помог", "поддерж", "бабушк", "вместе", "обращайтесь")):
            score += 0.6
    elif code == "ruler":
        if re.search(r"\d+\s*%", txt) or "итог" in txt or "квартал" in txt:
            score += 0.5
        if "ответствен" in txt:
            score += 0.2
    elif code == "sage":
        if any(w in txt for w in ("данные", "стат", "анализ", "исследован", "тренд")):
            score += 0.6
    elif code == "everyman":
        if any(w in txt for w in ("прошёл", "встрет", "поговорил", "по своему", "двор")):
            score += 0.6
    elif code == "hero":
        if any(w in txt for w in ("добиваюсь", "контролирую", "буду", "решу", "сделаю")):
            score += 0.5
    elif code == "creator":
        if any(w in txt for w in ("запуска", "проект", "идея", "концепц")):
            score += 0.5
    elif code == "explorer":
        if any(w in txt for w in ("вернулся", "посмотрел", "побывал", "опыт")):
            score += 0.5
    elif code == "rebel":
        if any(w in txt for w in ("пора менять", "пересмотр", "не согласен", "застой")):
            score += 0.6
    elif code == "lover":
        if any(w in txt for w in ("красота", "ярмарк", "концерт", "впечатлен", "атмосфер")):
            score += 0.5
    elif code == "jester":
        if any(w in txt for w in ("если коротко", "кстати", ":)", "юмор")) or "—" in txt:
            score += 0.3
    elif code == "innocent":
        if any(w in txt for w in ("спасибо", "благодарю", "доброе", "светлый")):
            score += 0.5
    elif code == "magician":
        if any(w in txt for w in ("видение", "город — это", "будущее", "направлен")):
            score += 0.5

    return min(1.0, score)
