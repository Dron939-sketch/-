# ---------------------------------------------------------------------------
# Lookup helpers
# ---------------------------------------------------------------------------

def _normalize(s: str) -> str:
    return re.sub(r"\s+", " ", (s or "").strip().lower())


def find_person(query: str) -> Optional[Person]:
    """Грубый fuzzy-match: ищем фамилию или alias В тексте запроса.
    Запрос короче 5 символов почти не несёт информации — игнорим."""
    if not query:
        return None
    q = _normalize(query)
    if len(q) < 5:
        return None
    for p in ALL_PEOPLE:
        # 1. Совпадение фамилии (первое слово full_name) внутри query.
        last_name = (p.get("full_name", "") or "").split(" ", 1)[0].lower()
        if last_name and len(last_name) >= 4 and last_name in q:
            return p
        # 2. Любой alias целиком в query (≥ 4 символа)
        for alias in p.get("aliases", []) or []:
            an = _normalize(alias)
            if an and len(an) >= 4 and an in q:
                return p
    return None


# Ключевые triggers «кто тебя создал?» — детерминированный ответ
_CREATOR_TRIGGERS = re.compile(
    r"\b(кто (тебя |вас |тебе )?(создал|разработал|сделал|написал|"
    r"автор|разработчик|придумал|спроект)|твой (создатель|автор|разработчик)|"
    r"кем (ты )?создан|чей ты)",
    re.IGNORECASE | re.UNICODE,
)


def is_creator_question(text: str) -> bool:
    if not text or len(text) < 6:
        return False
    return bool(_CREATOR_TRIGGERS.search(text))


def kb_prompt_block() -> str:
    """Короткий блок для подмеса в system prompt — Джарвис «знает» этих
    людей и упоминает их если уместно. Без длинных биографий, чтобы не
    жечь токены."""
    lines: List[str] = ["База знаний (люди, которых ты знаешь):"]
    lines.append(f"- Создатель: {CREATOR['full_name']} — {CREATOR['bio']}")
    lines.append(f"- Глава города: {HEAD['full_name']}.")
    deputies_short = "; ".join(
        f"{d['full_name']}" for d in DEPUTIES_OF_HEAD
    )
    lines.append(f"- Заместители главы: {deputies_short}.")
    if COUNCIL_DEPUTIES:
        lines.append(
            f"- Совет депутатов: {len(COUNCIL_DEPUTIES)} человек, "
            f"включая председателя и двух заместителей. "
            f"При запросе по фамилии — отвечай конкретно."
        )
    return "\n".join(lines)


