def deterministic_creator_answer() -> str:
    """Когда вопрос «кто тебя создал?» — отвечаем без LLM, чтобы не
    зависеть от настроения модели."""
    return (
        f"Меня создал {CREATOR['full_name']} — "
        f"{CREATOR['bio']}"
    )


def deterministic_person_answer(person: Person) -> str:
    """Короткий ответ голосом по конкретному человеку."""
    role = person.get("role", "")
    bio = person.get("bio", "")
    return f"{person['full_name']} — {role}. {bio}".strip()
