def complaint_examples() -> List[Dict[str, Any]]:
    """Список 6 категорий для UI-карточек."""
    return [
        {"code": c["code"], "emoji": c["emoji"], "label": c["label"], "example": c["example"]}
        for c in COMPLAINT_CATEGORIES
    ]


def render_reply(category: str, archetype: Dict[str, Any]) -> str:
    """Готовый ответ-шаблон в архетипе. Если категории нет — общий ответ."""
    body = _BODY_BY_CATEGORY.get(category)
    if not body:
        return ""
    arch_code = archetype.get("code") or "everyman"
    tone = _ARCHETYPE_TONE.get(arch_code) or _ARCHETYPE_TONE["everyman"]
    return f"{tone['open']}\n\n{body}\n\n{tone['close']}"
