def situations_list() -> List[Dict[str, Any]]:
    """Список 6 ситуаций — для UI карточек."""
    return [
        {"code": s["code"], "emoji": s["emoji"], "label": s["label"], "hint": s["hint"]}
        for s in SITUATIONS
    ]


def build_action_plan(situation_code: str) -> Dict[str, Any]:
    """Полный план для одной ситуации."""
    book = _PLAYBOOKS.get(situation_code)
    if not book:
        return {}
    sit = next((s for s in SITUATIONS if s["code"] == situation_code), None)
    return {
        "code":       situation_code,
        "label":      sit["label"] if sit else situation_code,
        "emoji":      sit["emoji"] if sit else "▶",
        "steps":      [{"when": s[0], "do": s[1]} for s in book["steps"]],
        "media":      book["media"],
        "contacts":   book["contacts"],
        "post_kinds": book["post_kinds"],
    }
