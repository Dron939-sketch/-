def selection_for(party_code: str) -> Dict[str, Any]:
    """Возвращает данные отбора для конкретной партии."""
    cfg = _SELECTION.get(party_code) or _SELECTION["independent"]
    out = dict(cfg)
    target = max(1, int(out.get("target", 1)))
    current = int(out.get("current", 0))
    out["progress_pct"] = round(min(100, current * 100 / target), 1)
    out["data_kind"] = "demo"
    return out
