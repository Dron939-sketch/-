def build_persona(
    audit: Dict[str, Any], archetype: Dict[str, Any],
) -> Dict[str, Any]:
    """Расширенный портрет: 5 черт + headline + опасность + цитата."""
    code = archetype.get("code") or "everyman"
    trait_pack = _ARCHETYPE_TRAITS.get(code) or _ARCHETYPE_TRAITS["everyman"]

    # Подбираем цитату-эпиграф из лучших постов аудита (что_works)
    works = audit.get("what_works") or []
    epigraph = (works[0][:160] + "…") if works else None

    metrics = audit.get("metrics") or {}
    pp_week = float(metrics.get("posts_per_week") or 0)
    avg_len = float(metrics.get("avg_length") or 0)

    # Дополнительные читаемые штрихи из метрик
    style_hints: List[str] = []
    if pp_week >= 3:
        style_hints.append("Регулярна — заметна в ленте")
    elif pp_week < 1:
        style_hints.append("Редкие посты — теряет узнавание")
    if avg_len < 150:
        style_hints.append("Пишет коротко — мобильно, но фактуры мало")
    elif avg_len > 1200:
        style_hints.append("Любит лонгриды — детально, но требует внимания")

    return {
        "headline":  trait_pack["headline"],
        "traits":    trait_pack["traits"],
        "reads_as":  trait_pack["reads_as"],
        "danger":    trait_pack["danger"],
        "style_hints": style_hints,
        "epigraph":  epigraph,
        "archetype_name": archetype.get("name"),
    }


