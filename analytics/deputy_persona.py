# ---------------------------------------------------------------------------

def build_personal_tasks(
    missions: List[Dict[str, Any]],
    plan: Optional[Dict[str, Any]] = None,
    audit: Optional[Dict[str, Any]] = None,
) -> List[Dict[str, Any]]:
    """Личный ToDo на неделю в матрице срочно × важно (упрощённая)."""
    items: List[Dict[str, Any]] = []
    # Миссии — важно/срочно
    for m in (missions or [])[:3]:
        items.append({
            "title":   m.get("title", ""),
            "subtitle": m.get("hint") or m.get("why", ""),
            "quadrant": "do_now",  # важно + срочно
            "kind":    "mission",
        })
    # План публикаций — важно, не срочно
    if plan and plan.get("items"):
        for it in plan["items"][:3]:
            items.append({
                "title":    f"{it.get('day','')}: {it.get('topic','')}",
                "subtitle": (it.get("draft") or "")[:100],
                "quadrant": "schedule",
                "kind":     "plan",
            })
    # Ответы на жалобы — срочно, не важно (в смысле — много, но точечно)
    items.append({
        "title":    "Просмотреть последние комментарии",
        "subtitle": "Открой VK, ответь на 3-5 свежих обращений.",
        "quadrant": "delegate",
        "kind":     "reply",
    })
    return items[:7]


# ---------------------------------------------------------------------------
# Идеи и пиар — топ-3 на эту неделю
# ---------------------------------------------------------------------------

def build_pr_ideas(
    archetype: Dict[str, Any],
    calendar: List[Dict[str, Any]],
    district_today: Dict[str, Any],
    timing: Dict[str, Any],
) -> List[Dict[str, Any]]:
    """3 готовые идеи: 1 из календаря + 1 из жалоб + 1 из аналитики."""
    ideas: List[Dict[str, Any]] = []
    # 1. Из ближайшего повода календаря
    soon = next((e for e in (calendar or []) if (e.get("days_until") or 99) <= 14), None)
    if soon:
        when = "сегодня" if soon.get("days_until") == 0 else \
               "завтра" if soon.get("days_until") == 1 else \
               f"через {soon.get('days_until')} дн" if soon.get("days_until") else "в этом сезоне"
        ideas.append({
            "icon":   "🗓",
            "kind":   "calendar",
            "title":  soon.get("title", ""),
            "when":   when,
            "action": soon.get("hint", ""),
            "wizard": "content",
        })
    # 2. Из приоритета округа (district_today)
    items = (district_today or {}).get("items") or []
    if items:
        first = items[0]
        ideas.append({
            "icon":   "🏘",
            "kind":   "district",
            "title":  f"{first.get('sector', '—')}: пост-разбор",
            "when":   "на этой неделе",
            "action": first.get("text", ""),
            "wizard": "content",
        })
    # 3. Из аналитики времени
    bc = (timing or {}).get("heatmap", {}).get("best_cell") or {}
    if bc:
        ideas.append({
            "icon":   "📊",
            "kind":   "timing",
            "title":  f"Главный пост — {bc.get('day')}, {bc.get('band')}",
            "when":   "лучшее окно недели",
            "action": f"Самый сильный жанр в твоём голосе — {(archetype.get('do') or [''])[0]}",
            "wizard": "content",
        })
    return ideas
