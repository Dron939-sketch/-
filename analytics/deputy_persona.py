def build_expectations(deputy: Dict[str, Any]) -> List[Dict[str, Any]]:
    """4-6 ключевых ожиданий в порядке приоритета."""
    sectors = list(deputy.get("sectors") or [])
    out: List[Dict[str, Any]] = []
    seen_titles: set = set()
    for s in sectors:
        rows = _SECTOR_EXPECTATIONS.get(s) or []
        for title, prio, why in rows:
            if title in seen_titles:
                continue
            seen_titles.add(title)
            out.append({"sector": s, "title": title, "priority": prio, "why": why})
    # Сортируем — high первыми
    order = {"high": 0, "medium": 1, "low": 2}
    out.sort(key=lambda x: order.get(x["priority"], 3))
    return out[:6]


