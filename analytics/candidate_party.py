# ---------------------------------------------------------------------------
# Default election date — единый день голосования (2-е воскресенье сентября)
# ---------------------------------------------------------------------------

def default_election_date(today: Optional[date] = None) -> date:
    """Единый день голосования в России — 2-е воскресенье сентября.
    Если уже прошёл в текущем году — берём следующий."""
    today = today or date.today()
    for year in (today.year, today.year + 1):
        sep1 = date(year, 9, 1)
        # Первое воскресенье сентября
        first_sun = sep1 if sep1.weekday() == 6 else date(year, 9, (7 - sep1.weekday()) + 1) if sep1.weekday() == 0 else date(year, 9, 7 - sep1.weekday() + 1)
        # Чтоб не запутаться — простой цикл
        d = date(year, 9, 1)
        while d.weekday() != 6:
            d = date(year, 9, d.day + 1)
        # d — первое воскресенье. Второе — +7 дней.
        second_sun = date(year, 9, d.day + 7)
        if second_sun > today:
            return second_sun
    return date(today.year + 1, 9, 14)


def days_until(target: date, today: Optional[date] = None) -> int:
    today = today or date.today()
    return (target - today).days


