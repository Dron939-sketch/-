def current_stage(days_until_election: int) -> Dict[str, Any]:
    """По дням до выборов определяем текущий этап. Берём первый, у которого
    until_election_days <= days_until_election. Если 0 или меньше — день голосования."""
    if days_until_election <= 0:
        return STAGES[-1]
    for stage in STAGES:
        if days_until_election >= stage["until_election_days"]:
            return stage
    return STAGES[-1]


