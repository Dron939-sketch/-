def team_summary() -> Dict[str, Any]:
    """Сводка по штабу: минимально необходимый состав + стоимость."""
    essentials = [r for r in TEAM_ROLES if r["essential"]]
    optionals = [r for r in TEAM_ROLES if not r["essential"]]
    min_payroll = sum(r["rate_per_month_rub"] for r in essentials)
    full_payroll = sum(r["rate_per_month_rub"] for r in TEAM_ROLES)
    return {
        "essentials":   essentials,
        "optionals":    optionals,
        "min_payroll":  min_payroll,
        "full_payroll": full_payroll,
    }


